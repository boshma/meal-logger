//src/pages/index.tsx
import { useUser, useClerk } from "@clerk/nextjs";
import { type NextPage } from "next";
import Head from "next/head";
import { useState } from "react";
import DatePicker from "react-datepicker";
import "react-datepicker/dist/react-datepicker.css";
import LoginPage from "~/components/loginpage";
import { MacroSummary, MealForm, MealLog } from "~/components/meals";

// Define the home page
const Home: NextPage = () => {
  const user = useUser();
  const [selectedDate, setSelectedDate] = useState(new Date());
  const [key, setKey] = useState(Date.now());

  // Return the home page content
  return (
    <>
      <Head>
        <title>Fitache</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex h-screen flex-col items-center justify-center">
        <div className="mb-4 flex flex-col items-center justify-center">
          {!!user.isSignedIn && (
            <div className="flex flex-col items-center">
              <DatePicker
                selected={selectedDate}
                onChange={(date: Date | null) => date && setSelectedDate(date)}
              />
              <MacroSummary selectedDate={selectedDate} />
            </div>
          )}
          {!user.isSignedIn && <LoginPage />}
          {!!user.isSignedIn && <SignOutButton />}
          {!!user.isSignedIn && (
            <MealForm
              selectedDate={selectedDate}
              refetchMealLog={() => setKey(Date.now())}
            />
          )}
          {!!user.isSignedIn && (
            <MealLog
              selectedDate={selectedDate}
              refetchMealLog={() => setKey(Date.now())}
              key={key}
            />
          )}
        </div>
      </main>
    </>
  );
};

// Component for the sign out button
const SignOutButton = () => {
  const { signOut } = useClerk();

  // Define the sign out handler
  const handleSignOut = () => {
    signOut().catch((error) => {
      console.error("Failed to sign out", error);
    });
  };

  // Return the sign out button
  return <button onClick={handleSignOut}>Sign out</button>;
};

export default Home;
